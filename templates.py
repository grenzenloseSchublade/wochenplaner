def get_template_names(lang: str = "de") -> dict[str, str]:
    """Return {key: display_name} for all templates."""
    name_key = "name_en" if lang == "en" else "name_de"
    return {k: str(v[name_key]) for k, v in TEMPLATES.items()}


def get_template_activities(key: str) -> list[Activity]:
    """Return a deep copy of template activities (with fresh UUIDs)."""
    import uuid

    tpl = TEMPLATES.get(key)
    if not tpl:
        return []
    return [
        Activity(
            id=str(uuid.uuid4()),
            name=a["name"],
            day=a["day"],
            start=a["start"],
            end=a["end"],
            color=a["color"],
        )
        for a in tpl["activities"]
    ]
